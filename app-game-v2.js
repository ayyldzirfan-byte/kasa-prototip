function kasamGameV2Reaction(input, fallback = "ok") {
  if (input && typeof input === "object") return { type: input.type || "emoji", data: input.data || (fallback === "ok" ? "✓" : "✕") };
  const value = String(input || "").trim();
  if (!value) return { type: "emoji", data: fallback === "ok" ? "✓" : "✕" };
  if (/^data:image\//i.test(value) || /^https?:\/\//i.test(value)) return { type: "gif", data: value };
  return { type: "emoji", data: value };
}

function kasamGameV2FourOptions(options, actualTitle) {
  const list = [actualTitle, ...(Array.isArray(options) ? options : [])].map((item) => kasamCleanText(item || "")).filter(Boolean);
  const suggestions = ["Market", "Fatura", "Kira", "Yemek", "Ulasim", "Kahve", "Tatil", "Saglik", "Giyim", "Diger"];
  suggestions.forEach((item) => list.push(item));
  return [...new Set(list)].slice(0, 4);
}

function kasamGameV2ProjectScores(project = activeProject()) {
  if (!project) return {};
  project.familiarityScores = project.familiarityScores && typeof project.familiarityScores === "object" ? project.familiarityScores : {};
  return project.familiarityScores;
}

function kasamGameV2Score(project, guesserId, targetId, delta) {
  if (!project || !guesserId || !targetId) return 0;
  const scores = kasamGameV2ProjectScores(project);
  scores[guesserId] = scores[guesserId] && typeof scores[guesserId] === "object" ? scores[guesserId] : {};
  scores[guesserId][targetId] = Number(scores[guesserId][targetId] || 0) + Number(delta || 0);
  return scores[guesserId][targetId];
}

