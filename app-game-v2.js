function kasamGameV2ReactionHtml(reaction) {
  const value = reaction?.data || "";
  if (!value) return `<span class="game-reaction-emoji">✓</span>`;
  if (reaction.type === "photo" || reaction.type === "gif" || /^data:image\//i.test(value) || /^https?:\/\//i.test(value)) {
    return `<img class="game-reaction-image" src="${kasamGameV2SafeMedia(value)}" alt="Tepki" />`;
  }
  return `<span class="game-reaction-emoji">${kasamSafe(value, 24)}</span>`;
}

function kasamGameV2SafeMedia(value) {
  const text = String(value || "").trim();
  if (/^data:image\/(png|jpeg|jpg|webp|gif|svg\+xml)/i.test(text)) return kasamEscape(text);
  return kasamSafeUrl(text);
}

