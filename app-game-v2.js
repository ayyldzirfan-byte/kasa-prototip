function kasamGameV2Confetti(canvas) {
  if (!canvas) return;
  const context = canvas.getContext("2d");
  const rootStyle = typeof getComputedStyle === "function" ? getComputedStyle(document.documentElement) : null;
  const colors = ["--color-accent", "--color-income", "--color-expense", "--color-bg", "--color-text-primary"]
    .map((token) => rootStyle?.getPropertyValue(token).trim())
    .filter(Boolean);
  if (!colors.length) colors.push("currentColor");
  const pieces = Array.from({ length: 50 }, () => ({ x: Math.random() * canvas.width, y: -20, vx: (Math.random() - 0.5) * 5, vy: 2 + Math.random() * 5, r: Math.random() * 6 + 3, color: colors[Math.floor(Math.random() * colors.length)], rot: Math.random() * Math.PI }));
  const start = performance.now();
  function frame(now) {
    context.clearRect(0, 0, canvas.width, canvas.height);
    pieces.forEach((piece) => {
      piece.x += piece.vx;
      piece.y += piece.vy;
      piece.rot += 0.08;
      context.save();
      context.translate(piece.x, piece.y);
      context.rotate(piece.rot);
      context.fillStyle = piece.color;
      context.fillRect(-piece.r / 2, -piece.r / 2, piece.r, piece.r * 1.7);
      context.restore();
    });
    if (now - start < 2500) requestAnimationFrame(frame);
  }
  requestAnimationFrame(frame);
}

